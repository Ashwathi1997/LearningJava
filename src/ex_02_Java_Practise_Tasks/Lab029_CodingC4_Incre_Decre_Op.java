package ex_02_Java_Practise_Tasks;

public class Lab029_CodingC4_Incre_Decre_Op {
    public static void main(String[] args) {
        int i = 5;

        System.out.print("Original: "+i+" Pre-increment: "+ ++i +
                " Post-increment: "+ i++ +" (returned 6) Pre-decrement: "+ (--i) +
                " Post-decrement: " + (i--) +"returned 5");
    }
}
