package ex_04_Operators;

public class Lab034_Arithmetic_Operators {
    public static void main(String[] args) {
        // arithmetic operators
        // + addition
        // - subtraction
        // * multiplication
        // / division
        // % modulus
        // | modulus remainder
        int a = 20;
        int b = 3;
        float c = 3.0f;
        System.out.println(a+b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a/c);
        System.out.println(a-b);

        System.out.println("a+b");
    }

}
