package ex_04_Operators;

public class Lab037_Relational_Operator {
    public static void main(String[] args) {
//        < less than
//        <= less than or equal to
//        > greater
//        >= greater than equal to
//        == equal to (for checking)
//        != not equal

        int a = 10;
        int b = 30;
        boolean c = a > b; // give true or false as result // 10>30
        System.out.println(c);

        int age_ash = 29;
        int age_mamta = 32;
        boolean result = age_ash <= age_mamta;
        System.out.println(result);

    }
}
