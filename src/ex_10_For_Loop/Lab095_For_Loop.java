package ex_10_For_Loop;

public class Lab095_For_Loop {
    public static void main(String[] args) {
        //ICU - Initialization, Condition, Updation(Incre/Decrement)
        for (int i = 0; i < 10; i++) //even if i++ or ++i it doesnt matter
        {
            System.out.println(i);
        }
    }
}
