package ex_10_For_Loop;

public class Lab100_For_If_Loop {
    public static void main(String[] args) {
        for (int i = 0; i < 18; i++) {
            if (i > 15) {
                System.out.println("Gift - Iphone");
            } else {
                System.out.println("No Gift");
            }
        }
    }
}
